package model.insurance.info;

public class TrainPlan {
	private String plan;
	private int budget;
	public TrainPlan() {
		
	}
	public String getPlan() {
		return plan;
	}
	public void setPlan(String plan) {
		this.plan = plan;
	}
	public int getBudget() {
		return budget;
	}
	public void setBudget(int budget) {
		this.budget = budget;
	}
	
}
